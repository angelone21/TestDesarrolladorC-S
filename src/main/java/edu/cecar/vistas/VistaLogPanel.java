    private void bt_EntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_EntrarActionPerformed
        try {
            boolean flag = false;
            String path = System.getProperty("user.dir");
            File archivo_credenciales = new File(path + "\\Archivos\\archivo_credenciales.txt");
            String contenido;
            try (PrintWriter pw = new PrintWriter(archivo_credenciales)) {
                pw.println(text_Usuario.getText());
                pw.println(pass_Contraseña.getText());
            }
            controladorCliente.PeticionCliente("login");
            File respuesta_login = new File(path + "\\Archivos\\respuesta_login.txt");
            try (FileReader lector = new FileReader(respuesta_login)) {
                BufferedReader bf = new BufferedReader(lector);
                while ((contenido = bf.readLine()) != null) {
                    if (contenido.equals("true")) {
                        flag = true;
                        break;
                    }
                }
                if (flag == true) {
                    bf.close();
                    lector.close();
                    dispose();
                    java.awt.EventQueue.invokeLater(new Runnable() {
                        public void run() {
                            new VistaPrincipal().setVisible(true);
                        }
                    });
                } else {
                    JOptionPane.showMessageDialog(null, "Credenciales incorrectas.");
                }
            }
        } catch (Exception ex) {

            JOptionPane.showMessageDialog(null, "No hay conexion con la base de datos, intente nuevamente.");
        }

    }//GEN-LAST:event_bt_EntrarActionPerformed

