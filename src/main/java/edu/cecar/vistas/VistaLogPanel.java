    private void bt_registroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_registroActionPerformed
        // TODO add your handling code here:
        try {
            boolean flag = false;
            String path = System.getProperty("user.dir");
            File archivo_registro = new File(path + "\\Archivos\\archivo_registro.txt");
            String contenido;
            try (PrintWriter pw = new PrintWriter(archivo_registro)) {
                pw.println(text_UsuarioRegistro.getText());
                pw.println(pass_ContraseñaRegistro.getText());
            }
            controladorCliente.PeticionCliente("registro");
            File respuesta_registro = new File(path + "\\Archivos\\respuesta_registro.txt");
            try (FileReader lector = new FileReader(respuesta_registro)) {
                BufferedReader bf = new BufferedReader(lector);
                while ((contenido = bf.readLine()) != null) {
                    if (contenido.equals("true")) {
                        flag = true;
                        break;
                    }
                }
                if (flag == true) {
                    JOptionPane.showMessageDialog(null, "Registro completado.");
                    panel_registro.setVisible(false);
                    panel_entrar.setVisible(true);
                } else {
                    archivo_registro.delete();
                    respuesta_registro.delete();
                    JOptionPane.showMessageDialog(null, "Error datos erroneos o faltantes.");
                }
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "No hay conexion con la base de datos, intente nuevamente.");
        }
    }//GEN-LAST:event_bt_registroActionPerformed

